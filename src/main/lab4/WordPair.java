package main.lab4;

public class WordPair
{
    private String _first;
    private String _second;

    public WordPair(String first, String second)
    {
        _first = first;
        _second = second;
    }

    public String getFirst()
    {
        return _first;
    }

    public String getSecond()
    {
        return _second;
    }
}
